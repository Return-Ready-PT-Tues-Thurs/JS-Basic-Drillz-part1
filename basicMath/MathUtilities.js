class MathUtilities {


    add(num1, num2){
        let addCalc = num1 + num2;
        return (addCalc);
      }

    subtract(num1, num2){
        let subCalc = num1 - num2;
        return (subCalc);
      }

    divide(num1, num2){
        let divCalc = num1 / num2;
        return (divCalc);
      }

    multiply(num1, num2){
        let multCalc = num1 * num2;
        return (multCalc);
      }
}

module.exports = MathUtilities;